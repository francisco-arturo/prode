function getTodayDateKey() {
  var d = new Date();
  return d.getFullYear() + "-" +
    String(d.getMonth() + 1).padStart(2, "0") + "-" +
    String(d.getDate()).padStart(2, "0");
}

function buildMatchCatalog() {
  if (_matchCatalog) return _matchCatalog;

  var list = [];
  GROUPS.forEach(function (group) {
    group.matches.forEach(function (m) {
      var sch = MATCH_SCHEDULE[m.id] || {};
      list.push({
        id: m.id,
        date: sch.date || "",
        time: sch.time || "",
        phase: "Grupo " + group.id,
        type: "group",
        groupMatch: m
      });
    });
  });

  KNOCKOUT.forEach(function (def) {
    var sch = MATCH_SCHEDULE[def.id] || {};
    list.push({
      id: def.id,
      date: sch.date || "",
      time: sch.time || "",
      phase: def.round,
      type: "knockout",
      koDef: def
    });
  });

  _matchCatalog = list;
  return list;
}

function enrichKnockoutEntry(entry, bracket) {
  var info = bracket.matches[entry.id] || {};
  return {
    id: entry.id,
    date: entry.date,
    time: entry.time,
    phase: entry.phase,
    type: "knockout",
    koDef: entry.koDef,
    koInfo: info
  };
}

function getMatchesForDate(dateKey, predictions) {
  var bracket = resolveBracket(predictions || {});
  return buildMatchCatalog()
    .filter(function (m) { return m.date === dateKey; })
    .map(function (m) {
      if (m.type === "knockout") return enrichKnockoutEntry(m, bracket);
      return m;
    })
    .sort(function (a, b) { return a.time.localeCompare(b.time); });
}

function shiftScheduleDate(dateKey, deltaDays) {
  var d = new Date(dateKey + "T12:00:00");
  d.setDate(d.getDate() + deltaDays);
  return d.getFullYear() + "-" +
    String(d.getMonth() + 1).padStart(2, "0") + "-" +
    String(d.getDate()).padStart(2, "0");
}

function getDefaultScheduleDate() {
  return getFeaturedDayMatches({}).date;
}

function getMatchesForSelectedDay(dateKey, predictions) {
  return {
    date: dateKey,
    matches: getMatchesForDate(dateKey, predictions),
    isToday: dateKey === getTodayDateKey()
  };
}

function getFeaturedDayMatches(predictions) {
  var today = getTodayDateKey();
  var todayMatches = getMatchesForDate(today, predictions);
  if (todayMatches.length) {
    return { date: today, matches: todayMatches, isToday: true };
  }

  var upcoming = buildMatchCatalog()
    .filter(function (m) { return m.date && m.date >= today; })
    .sort(function (a, b) {
      if (a.date !== b.date) return a.date.localeCompare(b.date);
      return a.time.localeCompare(b.time);
    });

  if (upcoming.length) {
    var nextDate = upcoming[0].date;
    return {
      date: nextDate,
      matches: getMatchesForDate(nextDate, predictions),
      isToday: false
    };
  }

  var all = buildMatchCatalog()
    .filter(function (m) { return m.date; })
    .sort(function (a, b) {
      if (a.date !== b.date) return a.date.localeCompare(b.date);
      return a.time.localeCompare(b.time);
    });
  var lastDate = all[all.length - 1].date;
  return {
    date: lastDate,
    matches: getMatchesForDate(lastDate, predictions),
    isToday: false
  };
}

function formatScheduleDayLabel(dateKey, isToday) {
  var d = new Date(dateKey + "T12:00:00");
  var long = d.toLocaleDateString("es", {
    weekday: "long",
    day: "numeric",
    month: "long"
  });
  var today = getTodayDateKey();
  if (dateKey === today || isToday) return "Hoy · " + long;
  return long;
}

function formatScheduleDayMeta(dateKey, matchCount, isToday) {
  var label = formatScheduleDayLabel(dateKey, isToday);
  if (!matchCount) return label + " · sin partidos";
  return label + " · " + matchCount + " partido" + (matchCount === 1 ? "" : "s");
}

function formatScheduleTime(timeStr) {
  if (!timeStr) return "";
  return timeStr.replace(":", ".") + " hs";
}

// Hora de arranque del partido (ms epoch). Usa la hora local del navegador.
function getMatchKickoffMs(matchId) {
  var sch = MATCH_SCHEDULE[matchId];
  if (!sch || !sch.date || !sch.time) return null;
  return new Date(sch.date + "T" + sch.time + ":00").getTime();
}

// True si el partido ya arrancó (o debería haber arrancado según el calendario).
function isMatchPastKickoff(matchId) {
  var kickoff = getMatchKickoffMs(matchId);
  if (kickoff === null) return false;
  return Date.now() >= kickoff;
}

// Milisegundos hasta el próximo arranque futuro, o null si no hay ninguno.
function msUntilNextKickoff() {
  var now = Date.now();
  var next = null;
  buildMatchCatalog().forEach(function (m) {
    var t = getMatchKickoffMs(m.id);
    if (t !== null && t > now && (next === null || t < next)) next = t;
  });
  return next === null ? null : next - now;
}
